    <script>
        $(document).ready(function() {
            read()
        });
        // Read Database
        function read() {
            $.get("{{ url('read') }}", {}, function(data, status) {
                $("#read").html(data);
            });
        }
        // Untuk modal halaman create
        function create() {
            $.get("{{ url('create') }}", {}, function(data, status) {
                $("#exampleModalLabel").html('Create Product')
                $("#page").html(data);
                $("#exampleModal").modal('show');

            });
        }

        // untuk proses create data
        function store() {
            var name = $("#name").val();
            var category = $("#category").val();
            var price = $("#price").val();

            $.ajax({
                type: "get",
                url: "{{ url('store') }}",
                data: {name,category,price},
                success: function(data) {
                    $(".btn-close").click();
                    read()
                }
            });
        }

        // Untuk modal halaman edit show
        function show(id) {
            $.get("{{ url('show') }}/" + id, {}, function(data, status) {
                $("#exampleModalLabel").html('Edit Product')
                $("#page").html(data);
                $("#exampleModal").modal('show');
            });
        }

        // untuk proses update data
        function update(id) {
            var name = $("#name").val();
            var category = $("#category").val();
            var price = $("#price").val();
            $.ajax({
                type: "get",
                url: "{{ url('update') }}/" + id,
                data: {name,category,price},
                success: function(data) {
                    $(".btn-close").click();
                    read()
                }
            });
        }

        // untuk delete atau destroy data
        function destroy(id) {

            $.ajax({
                type: "get",
                url: "{{ url('destroy') }}/" + id,
                data: "name=" + name,
                success: function(data) {
                    $(".btn-close").click();
                    read()
                }
            });
        }
